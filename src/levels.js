function generateWorld(level) {
  const idx = (level.id - 1) % LEVEL_MAPS.length;
  const mapStr = LEVEL_MAPS[idx];
  const ROWS = mapStr.length;
  const COLS = mapStr[0].length;
  const tiles = Array.from({ length: ROWS }, () => Array(COLS).fill(0));

  for (let r = 0; r < ROWS; r++) {
    for (let c = 0; c < COLS; c++) {
      tiles[r][c] = mapStr[r][c] === '#' ? 1 : 0;
    }
  }

  const decor = [];
  const decorTypes = level.groundDecor || ['grave'];
  for (let r = 1; r < ROWS - 1; r++) {
    for (let c = 1; c < COLS - 1; c++) {
      if (tiles[r][c] === 1 && tiles[r - 1][c] === 0 && Math.random() < 0.12) {
        decor.push({
          x: c * TILE,
          y: (r - 1) * TILE,
          type: decorTypes[Math.floor(Math.random() * decorTypes.length)]
        });
      }
    }
  }

  return { tiles, COLS, ROWS, decor };
}